package Basics04072018;

public class VariablesEx {
	
	public static void main(String[] args) {
		
		int age; //variable is declared
		
		age = 30; //variable is initialized
		
		double salary = 2056.25; //variable is declared and initialized
		
		char Gender = 'M'; //char should be in single quote
		
		String name = "Karthick"; //String should be in double quotes
		
		boolean PolicyStatus = true;
		
		System.out.println("Policy holder name : " + name);
		System.out.println("Age of Policy holder : " + age);
		System.out.println("Gender of Policy holder : " + Gender);
		System.out.println("Salary of Policy holder : " + salary);
		System.out.println("Active Status of Policy is : " + PolicyStatus);
		
	}

}
